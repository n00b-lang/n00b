/*
 * test_rsa_pss_roundtrip.c — exercises the RSA-PSS-SHA256 sign + verify
 * primitives in lockstep.  Hardcoded RSA-2048 keypair (extracted from
 * `openssl genrsa -out rsa.pem 2048`) so the test is deterministic;
 * salt bytes are also fixed.
 *
 * Coverage:
 *   - sign(msg, n, d) → bytes; verify(msg, n, e, sig) → OK.
 *   - Wrong message → verify rejects.
 *   - Bit-flipped signature → verify rejects.
 *   - Wrong public key → verify rejects.
 *
 * This is the math-level test for `n00b_rsa_sign_pss_sha256` /
 * `n00b_rsa_verify_pss_sha256` and provides the lower-bound guarantee
 * for RSA mTLS client-auth: whatever secret provider implements
 * `n00b_quic_secret_sign(..., N00B_QUIC_SIG_RSA_PSS_2048)` can return
 * signatures generated by this primitive, and the picotls
 * verify_certificate path (now wired for RSA-PSS via
 * `picotls_certverify.c::client_sign_cb`) will accept them on the
 * wire.
 *
 * Full handshake-time RSA mTLS interop requires a real RSA-capable
 * secret provider (the current ephemeral provider only generates
 * ECDSA keys) plus an RSA cert fixture — separate follow-up work.
 */

