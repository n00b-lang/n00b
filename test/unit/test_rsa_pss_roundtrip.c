static void
test_roundtrip(void)
{
    const char *msg = "n00b RSA-PSS-SHA256 round-trip test message.";
    uint8_t     sig[256];
    size_t      sig_len = sizeof(sig);

    int rc = n00b_rsa_sign_pss_sha256(rsa_n, sizeof(rsa_n),
                                       rsa_d, sizeof(rsa_d),
                                       salt,  sizeof(salt),
                                       (const uint8_t *)msg, strlen(msg),
                                       sig, &sig_len);
    assert(rc == N00B_QUIC_OK);
    assert(sig_len == sizeof(rsa_n));

    rc = n00b_rsa_verify_pss_sha256(rsa_n, sizeof(rsa_n),
                                     rsa_e, sizeof(rsa_e),
                                     (const uint8_t *)msg, strlen(msg),
                                     sig, sig_len);
    assert(rc == N00B_QUIC_OK);
    printf("  [PASS] sign + verify round-trip\n");
}

static void
test_wrong_message(void)
{
    const char *msg_a = "message A";
    const char *msg_b = "message B";
    uint8_t     sig[256];
    size_t      sig_len = sizeof(sig);

    int rc = n00b_rsa_sign_pss_sha256(rsa_n, sizeof(rsa_n),
                                       rsa_d, sizeof(rsa_d),
                                       salt,  sizeof(salt),
                                       (const uint8_t *)msg_a, strlen(msg_a),
                                       sig, &sig_len);
    assert(rc == N00B_QUIC_OK);
    rc = n00b_rsa_verify_pss_sha256(rsa_n, sizeof(rsa_n),
                                     rsa_e, sizeof(rsa_e),
                                     (const uint8_t *)msg_b, strlen(msg_b),
                                     sig, sig_len);
    assert(rc != N00B_QUIC_OK);
    printf("  [PASS] verify rejects wrong message\n");
}

static void
test_bitflip(void)
{
    const char *msg = "bitflip test";
    uint8_t     sig[256];
    size_t      sig_len = sizeof(sig);

    int rc = n00b_rsa_sign_pss_sha256(rsa_n, sizeof(rsa_n),
                                       rsa_d, sizeof(rsa_d),
                                       salt,  sizeof(salt),
                                       (const uint8_t *)msg, strlen(msg),
                                       sig, &sig_len);
    assert(rc == N00B_QUIC_OK);
    sig[128] ^= 0x01;  /* flip a bit in the middle of the signature */
    rc = n00b_rsa_verify_pss_sha256(rsa_n, sizeof(rsa_n),
                                     rsa_e, sizeof(rsa_e),
                                     (const uint8_t *)msg, strlen(msg),
                                     sig, sig_len);
    assert(rc != N00B_QUIC_OK);
    printf("  [PASS] verify rejects bit-flipped signature\n");
}

int
main(int argc, char **argv)
{
    n00b_runtime_t rt;
    n00b_init(&rt, argc, argv);

    printf("test_rsa_pss_roundtrip:\n");
    test_roundtrip();
    test_wrong_message();
    test_bitflip();
    printf("All test_rsa_pss_roundtrip tests passed.\n");

    n00b_shutdown();
    return 0;
}
