// RX accepts a brace-enclosed list of {lo, hi} pairs and forwards to
// mk_ranges_u8 with the implied length.
#define RX(...)           regex_builder_mk_ranges_u8(b, \
                              (const range_u8_t[]) __VA_ARGS__, \
                              sizeof((const range_u8_t[]) __VA_ARGS__) \
                                  / sizeof(range_u8_t))
#define CONCAT(a, c)      regex_builder_mk_concat(b, (a), (c))
#define UNION(a, c)       regex_builder_mk_union(b, (a), (c))

