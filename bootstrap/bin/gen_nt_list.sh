#!/bin/bash
# Generate sorted NT_LIST macro from parse.c and parse_support.c.
#
# Usage: gen_nt_list.sh <parse.c> <parse_support.c>
#
# Extracts non-terminal names from:
#   - declare_nt(name, N)        in parse.c
#   - declare_recursive(name, N) in parse.c
#   - node_alloc(ctx, "name")    in parse_support.c (terminal productions)
#
# Outputs a complete #define NT_LIST(X) macro, sorted for binary search.

set -euo pipefail

parse_c="$1"
parse_support_c="$2"

# Extract NT names from declare_nt/declare_recursive calls
declared=$(grep -E '^declare_(nt|recursive)\(' "$parse_c" \
    | sed -E 's/declare_(nt|recursive)\(([a-zA-Z_]+),.*/\2/')

# Extract terminal NT names from node_alloc(ctx, "name") calls
terminals=$(grep -oE 'node_alloc\(ctx, "([a-zA-Z_]+)"\)' "$parse_support_c" \
    | sed -E 's/node_alloc\(ctx, "([a-zA-Z_]+)"\)/\1/')

# Combine, deduplicate, and sort
all_nts=$(printf '%s\n' $declared $terminals | sort -u)

# Find longest name for alignment
max_len=0
for name in $all_nts; do
    len=${#name}
    if (( len > max_len )); then
        max_len=$len
    fi
done

# Pad to align the backslashes: "    X(" + name + ")" + padding + "\"
# Column = 4 + 2 + max_len + 1 + padding
col=$(( 4 + 2 + max_len + 1 + 1 ))

# Emit the macro
echo "// Auto-generated by scripts/gen_nt_list.sh — do not edit"
echo "//"
echo "// Sources: src/parse.c (declare_nt/declare_recursive)"
echo "//          src/parse_support.c (node_alloc terminals)"
printf '#define NT_LIST(X)'

for name in $all_nts; do
    entry="    X(${name})"
    padding=$(( col - ${#entry} ))
    if (( padding < 1 )); then
        padding=1
    fi
    printf ' \\\n%s%*s' "$entry" "$padding" ""
done

echo ""
